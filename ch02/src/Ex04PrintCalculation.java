
public class Ex04PrintCalculation {

	public static void main(String[] args) {
		//print() : 출력 후에 줄바꿈을 안함
		System.out.print("Hello");
		System.out.print("Hello");
		System.out.println("Hello");
		
		//println() : 출력 후에 줄바꿈을 함
		System.out.println("5+3");
		//사칙연산
		System.out.println(5+3);
		System.out.println(5-3);
		System.out.println(5*3);
		System.out.println(5/3);
		System.out.println(5/3.0);
	}

}
